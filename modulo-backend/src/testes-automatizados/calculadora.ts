export function soma (numero1: number, numero2: number) {
    return numero1 + numero2
}

export function subtracao (numero1: number, numero2: number) {
    return numero1 - numero2
}

export function multiplicacao (numero1: number, numero2: number) {
    return numero1 * numero2
}

export function divisao (numero1: number, numero2: number) {
    return numero1 / numero2
}
